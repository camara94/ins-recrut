    <h5>I.MISSIONS</h5>

    <p>
        Les travaux de terrain sont importants dans la phase de réalisation du recensement pilote.
        Le personnel en charge de ces travaux aura pour missions de :
    </p>

    <ul>
        <li>Contacter les autorités administratives et locales et les informer de l’opération du recensement pilote et obtenir leur coopération ;</li>
        <li>Faire la reconnaissance des ZD ;</li>
        <li>Faire la numérotation des concessions  et ménages ;</li>
        <li>Administrer les questionnaires ménages dans les concessions  ;</li>
        <li>Transférer les données collectées au serveur central suivant le protocole de transfert de données qui sera défini ;</li>
        <li>Soumettre un rapport de mission au BCR.</li>
    </ul>

    <h5>II.PROFIL DU CANDIDAT </h5>

    <ul>
            <li>être  de nationalité guinéenne ;</li>
            <li>être âgé de 18 ans au minimum et de 45 ans au maximum, au moment du dépôt du dossier de candidature ; </li>
            <li>Avoir un niveau minimum BAC  ;</li>
            <li>Avoir une expérience en matière de collecte digitale notamment sur tablettes et smartphones est un atout ;</li>
            <li>être en bonne santé physique, mentale et intellectuelle ;</li>
            <li>être capable de travailler en équipe et sous pression ;</li>
            <li>avoir des aptitudes à communiquer couramment en français et au moins dans une langue locale ; </li>
            <li>être de bonne moralité ;</li>
            <li>avoir un bon sens des relations humaines ;</li>
            <li>avoir le respect du secret professionnel ;</li>
            <li>avoir un sens de l’organisation et l’esprit d’analyse ;</li>
            <li>être dynamique, rigoureux, disponible et ponctuel ;</li>
            <li>être capable et disponible de travailler en milieu rural sous différentes intempéries ;</li>
            <li>être capable de rédiger un rapport.</li>
    </ul>

    <h5>III.PREALABLES A LA PARTICIPATION A L’ACTIVITE DE COLLECTE</h5>
   
    <ul>
        <li>a) Etre résident ou ressortissant de la sous préfecture pilote ou dans une sous préfecture avoisine ou dans la préfecture de tutelle ;</li>
        <li>b) Le candidat doit être entièrement libre de tout engagement sur la durée de l’activité. La formation va durer vingt et un (15) jours et la collecte s’étendra sur la période de mai 2024 à juin 2024 pour une durée de 21 jours.</li>
        <li>c) Ils percevront les frais de transport et un service de restauration sera assuré pendant la formation.</li>
        <li>d) Pendant la phase de collecte, les agents seront affectés dans les zones pilotes retenues par le BCR.</li>
        <li>e) Les dispositions seront prises par le BCR pour assurer le déplacement des agents pendant la phase de collecte.</li>
    </ul>


    <h5>IV.	DOSSIER DE CANDIDATURE</h5>

    <p>Le dossier de candidature comprendra les documents suivants : </p>
    <ul>
        <li>Une photo d’identité; </li>
        <li>une copie du diplôme ;</li>
        <li>un Curriculum Vitae détaillé ;</li>
        <li>une photocopie de la pièce d’identité (Carte Nationale d’Identité, Recepissé d’Identité, Passeport);</li>
        <li>un certificat de visite et de contre visite médical;</li>
        <li>une pièce d’identité de la personne garante.</li>
    </ul>

