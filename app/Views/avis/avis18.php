    <h3>AVIS DE RECRUTEMENT DE CHAUFFEURS DANS LE CADRE DE LA CARTOGRAPHIE CENSITAIRE PRINCIPALE</h3>
    <p> 
        Dans le cadre de la réalisation du quatrième Recensement Général de la Population et de l’Habitation (RGPH-4), l‘Institut National de la Statistique (INS), 
        à travers le Bureau Central du Recensement (BCR) recherche 65 Chauffeurs.    
    </p>

    <h3>I.	 PROFIL DU CANDIDAT</h3>

    <ul>
        <li> être de nationalité guinéenne ; </li> 
        <li> être âgé de 21 ans au minimum et de 55 ans au maximum, au moment du dépôt du dossier de candidature ;  </li> 
        <li> avoir un permis de conduire en cours de validité ;</li> 
        <li> être en bonne santé physique, mentale et intellectuelle ; </li> 
        <li> être capable de travailler sous pression ;</li> 
        <li> savoir lire et écrire en français ; </li> 
        <li> être de bonne moralité ;</li> 
        <li> avoir un bon sens des relations humaines ;</li> 
        <li> avoir le respect de la hiérarchie et du secret professionnel ;</li> 
        <li> être dynamique, rigoureux, disponible et ponctuel ;</li> 
        <li> être disposé à travailler partout sur l’ensemble du territoire national ;</li> 
        <li> avoir une expérience avec les institutions internationales est un atout</li> 
    </ul>

    <h3>DOSSIER DE CANDIDATURE</h3>
    <p> Le dossier de candidature comprendra les documents suivants : </p>
    <ul>
        <li>Une photo d’identité; </li>
        <li>Une copie du permis de conduire en cours de validité ; </li>
        <li>Une copie de la pièce d’identité (Carte Nationale d’Identité, Recepissé d’Identité, Passeport, carte d’électeur); </li>
        <li>Une copie de la pièce d’identité de la personne à contacter en cas d’urgence. </li>
        <!-- <li>Un certificat de visite et de contre visite médicale; </li> -->
    </ul>

    <h3>III. DATE ET LIEU DE DEPÔT DES DOSSIERS DE CANDIDATURES </h3>
    <p>
        Les inscriptions se feront en ligne à travers le lien suivant : <a href="https://rgph.gov.gn/">https://rgph.gov.gn/</a> 
        du 16 au 26 Novembre 2023. <br>
        Chaque document suscité devra être scanné sous format PDF ou JPEG avec une taille maximale de 2 Mo.
    </p>
    <p> La date limite du dépôt des dossiers est fixée pour <strong style="font-weight:bold">26 Novembre 2023, à 00H délai de rigueur</strong>. L’heure de l’enregistrement sur les sites faisant foi.</p>

    <p>Seuls les candidats présélectionnés seront contactés pour le test de sélection final.</p>

